#Desenvolva um programa que leia seis números inteiros
# e mostre a soma apenas daqueles que forem pares.
# Se o valor digitado for ímpar, desconsidere-o.

soma = 0
contador = 0

for n in range (1,7):
    num = int(input(f'Modelo numero {n} :'))
    if num % 2 == 0 :
        soma += num
        contador += 1


print(f'A soma entre todos os numeros pares é {soma}'
      f' E o numero de todos os numeros pares é {contador}')